function SetImageUrl(obj,imgUrl){
  try{
    // ボタン画像入れ替え
    obj.src = imgUrl;
  }catch(ex){
    // 例外発生時の処理
    top.FUNCTION_FRAME.Public_ExceptionMessage(ex);
  }
}
